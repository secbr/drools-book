package com.secbro2.entity;

/**
 * @author sec
 * @version 1.0
 **/
public class Person {

	/**
	 * 年龄
	 */
	private int age;

	/**
	 * 得分
	 */
	private int score;

	/**
	 * 结果描述
	 */
	private String desc;

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}
}
